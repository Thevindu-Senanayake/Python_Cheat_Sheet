#addtion
print(10 + 5)

#subtraction
print(10 - 5)

#multipication
print(10 * 5)

#divition

#answer will be float
print(10 / 5)
print(10 / 3)
#answer will be integer or whole number
print(10 // 3)

#modulis
#answer will be remainder of division
print(10 % 3)

#exponent
print(10 ** 3)
#10 to the power 3

#increment
x = 15
x = x + 5
print(x)

#if we want to do such thing we can use Augmented Assignment Operators
x = 15
x += 5
print(x)

#decrement
x -= 5
print(x) 